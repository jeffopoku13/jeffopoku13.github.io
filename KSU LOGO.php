                        Partners </span><a style="font-size: 1.2rem;" href="https://trademarklicensing.kennesaw.edu/licensed_vendors.php" target="_blank" rel="noopener">https://trademarklicensing.kennesaw.edu/licensed_vendors.php</a><span style="font-size: 1.2rem;">.</span></p>
                  
                  <p>Official student organizations, are required to use a licensee to produce any merchandise
                     or apparel with the Kennesaw State name and/or trademarks. Student organizations must
                     request and obtain approval to use the university’s names, logos or symbols on commercial
                     goods and specialty items. This includes merchandise used within the organization
                     (staff shirts, pens, etc.), given as gifts, or sold. Registered student organizations
                     may represent themselves as agents of the university and use names, official logos
                     or symbols of the university with the written consent of Office of Strategic Communications
                     and Marketing. Requests and artwork must be submitted for approval to Office of Strategic
                     Communications and Marketing at <a href="https://designapproval.kennesaw.edu/" target="_blank" rel="noopener">https://designapproval.kennesaw.edu/</a>.&nbsp;
                  </p>
                  
                  <h4>Exemptions For Design Approval</h4>
                  
                  <p>Design approval is not required for academic materials produced for scholarly presentation
                     (e.g., peer-reviewed articles, poster presentations, oral presentations, PowerPoint
                     presentations, etc.).
                  </p>
                  
                  <p>For publications and materials of this nature that employ the university’s logo, the
                     author/creator is asked to comply fully with the university’s visual identity standards
                     by following the logo usage guidelines as outlined in the Official Logo Policy.
                  </p>
                  
                  <p>All materials that fall outside of this academic/scholarly work exemption should be
                     submitted to <a href="https://designapproval.kennesaw.edu/" target="_blank" rel="noopener">https://designapproval.kennesaw.edu/</a>.
                  </p>
                  
                  <h4>Student organizations</h4>
                  
                  <p>Registered student organizations (RSO) may represent themselves as agents of the university
                     and use official affiliate level, official logos or symbols of the university with
                     the written consent of Office of Strategic Communications and Marketing. Requests
                     and artwork must be submitted for approval to Office of Strategic Communications and
                     Marketing at <a href="https://designapproval.kennesaw.edu/" target="_blank" rel="noopener">https://designapproval.kennesaw.edu/</a>. Approval must be received prior to printing, manufacturing and distributing.
                  </p>
                  
                  <p>To assist in the approval process, the RSO must submit a copy of its current logo
                     at the time it applies for registration or renewal. Requests or artwork that do not
                     reflect the logo on file will not be approved.
                  </p>
                  
                  <p>Student organizations are not permitted to use the university’s official seal or KS
                     monogram that does not include institutional name lock-up.
                  </p>
                  
