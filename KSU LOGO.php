                  <h1>Logo Usage</h1>
                  <h3>Reproducing the KSU Logo</h3>
                  
                  <h4>Size recommendations</h4>
                  
                  <p>The university master brand is intended as a visual identifier, not a title block,
                     and should be scaled as such. In general publication usage, the logo should appear
                     no smaller than 1 inch and, should be prominently located.
                  </p>
                  
                  <p><img class="full" id="" src="/images/master_brand-01.png" alt="KSU Master Brand"></p>
                  
                  <h4>Protected Area</h4>
                  
                  <p>The protected area around the logo ensures that no other graphic elements or messaging
                     interfere with its clarity and integrity. The depth of the protected area is equivalent
                     to 1/6 height of the KS monogram portion of the logo.
                  </p>
                  
                  <p><img class="full" id="" src="/images/protected_area-01.png" alt="Master Brand Protected Area"></p>
                  
                  <h4>Publication, print media and Web use</h4>
                  
                  <p>Use of the university’s brand, that include university logo, name, and other brand
                     identifiers must be reviewed by Office of Strategic Communications and Marketing.
                     Please submit approval requests to <a href="https://designapproval.kennesaw.edu/" target="_blank" rel="noopener">https://designapproval.kennesaw.edu/</a>.
                  </p>
                  
                  <h4>Centers, Institutes and Programs Logo Usage</h4>
                  
                  <p>Centers, institutes, and specific programs may be given flexibility in terms of which
                     logo structure is adopted. Centers, institutes and specific programs that have a significant
                     brand presence can receive either a sub-brand logo or a brand extension.
                  </p>
                  
                  <p>In most cases, centers, institutes, and specific programs receive sub-brand logos.
                     However, if there is an association with a specific college or department, a request
                     can be submitted for a brand extension logo.
                  </p>
                  
