                  <h4><strong>Graphic Element </strong></h4>
                  
                  <p>Think of graphic elements as a brand stylistic extension. Once the big picture of
                     a brand’s messaging has been established, graphic elements serve as visual tools that
                     help carry out this message. Distinct from the singularity of a logo, graphic elements
                     can vary depending on their function, such as representing different categories or
                     sections on an organization's market materials, illustrating the different phases
                     of a company's work processes, or visually denoting different divisions within an
                     organization.
                  </p>
                  
                  <h4>Event Related Submissions</h4>
                  
                  <p>Events that have pre-existing branding and/or a connection to an overarching brand
                     for example Cancer Awareness and specific holidays that possess a broader perceived
                     theme are exempt concerning for secondary color guidelines. Specific graphics for
                     recurring events must change annually to avoid the perceived creation of new brand
                     identities.
                  </p>
                  
