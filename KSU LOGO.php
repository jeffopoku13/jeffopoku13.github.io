                  <h4><strong>Wordmark </strong></h4>
                  
                  <p>A wordmark or logotype is a special text-only typographic treatment of a program name
                     or product name used for identification and branding purposes. The wordmark can be
                     based off of a typeface that represents the brand.
                  </p>
                  
                  <p>Wordmarks are only to be used for strategic initiatives of the University. Wordmarks
                     must be approved by the Office of Strategic Communications and Marketing.
                  </p>
                  
